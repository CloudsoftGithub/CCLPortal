package COM.JambPracPortal.MODEL;

public class subjectreg {
  private int sid;
  
  private String subject;
  
  private int year;
  
  private int time;
  
  public int getSid() {
    return this.sid;
  }
  
  public String getSubject() {
    return this.subject;
  }
  
  public void setSubject(String subject) {
    this.subject = subject;
  }
  
  public int getYear() {
    return this.year;
  }
  
  public void setYear(int year) {
    this.year = year;
  }
  
  public int getTime() {
    return this.time;
  }
  
  public void setTime(int time) {
    this.time = time;
  }
}
